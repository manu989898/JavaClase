
public class Ejemplo01 {

	public static void main(String[] args) {

		// Declaramos variables:

		int edad;
		String nombre;
		String dni;
		boolean matriculado;

		// Inicializamos (dar valor) a las variables

		edad = 24;
		nombre = "Manu";
		dni = "43203270R";
		matriculado = true;
		// Mostramos Variables

		System.out.println("El nombre es: " + nombre);
		System.out.println("La edad es: " + edad);
		System.out.println("El dni es: " + dni);
		System.out.println("Estado de matricula de " + nombre + " es: " + matriculado);

	}

}
